package haidnor.jvm.test.demo;

public class demo_switch_table {
    public static void main(String[] args) {
        int i = 3;
        switch (i) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            case 4:
                System.out.println("4");
                break;
            default:
                System.out.println("0");
        }
    }
}
